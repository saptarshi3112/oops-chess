package main.Utility;

public class Index {
    private Integer x, y;

    public Index(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getX() {
        return this.x;
    }

    public Integer getY() {
        return this.y;
    }
}
